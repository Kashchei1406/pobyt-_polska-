export function calculateQuizScore(answerMap = {}) {
  let score = 0;
  for (let i = 1; i <= 7; i++) {
    const qName = `q${i}`;
    const value = answerMap[qName];
    if (!value) continue;
    const scores = QUIZ_SCORES[qName];
    if (scores && scores[value] !== undefined) score += scores[value];
  }
  return score;
}

export function getQuizResultByScore(score) {
  if (score >= 8) return { key: 'high', ...QUIZ_RESULTS.high };
  if (score >= 2) return { key: 'medium', ...QUIZ_RESULTS.medium };
  return { key: 'risk', ...QUIZ_RESULTS.risk };
}

